int main(void) {
    ll n, m;
    cin >> n >> m;
    vector<ll> a(n);
    for (auto i = 0; i < n; i++){
        cin >> a[i];
    }
    vector<ll> count(1500005,0);
    for (auto i = 0; i < m; i++){
        count[a[i]]++;
    }
    ll res;
    for (auto i = 0; i < 1500005; i++){
        if (count[i] == 0){
            res = i;
            break;
        }
    }
    dump(res,count);
    for (auto i = 1; i < n-m+1; i++){
        // dump(a[i-1],a[i+m]);
        count[a[i-1]]--;
        count[a[i+m-1]]++;
        if (count[a[i-1]] == 0 && a[i-1] < res){
            res = a[i-1];
        }
        dump(res,count);
    }
    cout << res << endl;
    return 0;
}